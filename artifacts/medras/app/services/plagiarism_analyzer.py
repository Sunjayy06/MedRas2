ProviderChoice = Literal["openai", "gemini", "auto"]


def _normalise_check_result(parsed: Dict[str, Any], *, fallback_word_count: int, model_used: str) -> Dict[str, Any]:
    """Coerce the LLM's JSON into the exact schema we promise to clients.

    Models occasionally drop a field or return scores as floats; this guard
    keeps the route handler ignorant of provider quirks.
    """
    overall = int(round(float(parsed.get("overall_score", 0))))
    ai_like = int(round(float(parsed.get("ai_likelihood", 0))))
    overall = max(0, min(100, overall))
    ai_like = max(0, min(100, ai_like))

    word_count = parsed.get("word_count")
    if not isinstance(word_count, int) or word_count <= 0:
        word_count = fallback_word_count

    summary = str(parsed.get("summary") or "").strip()

    flagged_raw = parsed.get("flagged_passages") or []
    flagged: list[Dict[str, Any]] = []
    if isinstance(flagged_raw, list):
        for item in flagged_raw[:8]:
            if not isinstance(item, dict):
                continue
            sev = str(item.get("severity") or "medium").lower()
            if sev not in ("low", "medium", "high"):
                sev = "medium"
            flagged.append({
                "text": str(item.get("text") or "").strip()[:240],
                "reason": str(item.get("reason") or "").strip()[:200],
                "severity": sev,
                "suggestion": str(item.get("suggestion") or "").strip()[:240],
            })

    return {
        "overall_score": overall,
        "ai_likelihood": ai_like,
        "word_count": word_count,
        "summary": summary,
        "flagged_passages": flagged,
        "model_used": model_used,
    }


def check_originality(text: str, *, provider: ProviderChoice = "auto") -> Dict[str, Any]:
    """Score originality + AI likelihood for ``text``.

    Strategy:
      * "auto" → try OpenAI first; if it raises, fall back to Gemini.
      * "openai" / "gemini" → use that provider only.
    """
    text = (text or "").strip()
    if not text:
        raise ValueError("text is empty")

    word_count = len(re.findall(r"\b\w+\b", text))

    if provider == "openai":
        parsed = _call_openai_json(CHECK_SYSTEM_PROMPT, text)
        return _normalise_check_result(parsed, fallback_word_count=word_count, model_used="openai")

    if provider == "gemini":
        parsed = _call_gemini_json(CHECK_SYSTEM_PROMPT, text)
        return _normalise_check_result(parsed, fallback_word_count=word_count, model_used="gemini")

    # auto — OpenAI first, Gemini fallback
    try:
        parsed = _call_openai_json(CHECK_SYSTEM_PROMPT, text)
        return _normalise_check_result(parsed, fallback_word_count=word_count, model_used="openai")
    except Exception as exc:  # noqa: BLE001
        log.warning("plagiarism.check openai failed, falling back to gemini: %s", exc)
        parsed = _call_gemini_json(CHECK_SYSTEM_PROMPT, text)
        return _normalise_check_result(parsed, fallback_word_count=word_count, model_used="gemini")


def reduce_plagiarism(
    text: str,
    *,
    provider: ProviderChoice = "auto",
    protected_terms: Optional[list[str]] = None,
) -> Dict[str, Any]:
    """Rewrite ``text`` to read more human and less templated.

    If ``protected_terms`` is supplied, those exact substrings are surfaced
    to the LLM as hard "do not paraphrase" constraints, AND we verify
    afterwards that each one still appears in the rewrite — any missing
    terms are returned in the ``preserved_terms_missing`` field so the UI
    can warn the user.
    """
    text = (text or "").strip()
    if not text:
        raise ValueError("text is empty")

    system_prompt = REDUCE_SYSTEM_PROMPT + _build_protected_terms_block(protected_terms)

    def _normalise(parsed: Dict[str, Any], *, model_used: str) -> Dict[str, Any]:
        rewritten = str(parsed.get("rewritten_text") or "").strip()
        if not rewritten:
            # Models occasionally return the rewrite under a different key.
            rewritten = str(parsed.get("text") or parsed.get("rewrite") or "").strip()
        # Treat an empty rewrite as a provider failure so the auto-fallback
        # path can try the other provider and the route layer can return a
        # 502 instead of a misleading 200 with blank output.
        if not rewritten:
            raise RuntimeError("Provider returned an empty rewrite.")
        changes = parsed.get("changes_made")
        if not isinstance(changes, int):
            try:
                changes = int(changes)
            except Exception:
                changes = 0
        # Verify protected terms survived the rewrite. Any that didn't are
        # surfaced for the UI to flag — we don't fail the whole call because
        # often the missing term IS still present, just with a paraphrased
        # boundary (e.g. trailing punctuation differs).
        missing: list[str] = []
        if protected_terms:
            for term in protected_terms:
                if term and term not in rewritten:
                    missing.append(term)
        return {
            "original_text": text,
            "rewritten_text": rewritten,
            "changes_made": max(0, changes),
            "notes": str(parsed.get("notes") or "").strip()[:400],
            "model_used": model_used,
            "protected_terms_count": len(protected_terms or []),
            "preserved_terms_missing": missing[:50],
        }

    if provider == "openai":
        return _normalise(_call_openai_json(system_prompt, text, max_tokens=4096), model_used="openai")
    if provider == "gemini":
        return _normalise(_call_gemini_json(system_prompt, text, max_tokens=8192), model_used="gemini")
    try:
        return _normalise(_call_openai_json(system_prompt, text, max_tokens=4096), model_used="openai")
    except Exception as exc:  # noqa: BLE001
        log.warning("plagiarism.reduce openai failed, falling back to gemini: %s", exc)
        return _normalise(_call_gemini_json(system_prompt, text, max_tokens=8192), model_used="gemini")


# ---------------------------------------------------------------------------
# 3-stage rewrite pipeline
# ---------------------------------------------------------------------------
#
# When the user clicks "Reduce plagiarism" on an analysed upload, we run
# each detected section through three sequential LLM stages:
#
#   A. Paraphrase-for-originality    — primary: OpenAI gpt-4o
#   B. De-AI / humanise              — primary: Gemini gemini-2.5-flash
#   C. Quality-and-flow polish pass  — primary: OpenAI gpt-4o
#
# Each stage falls back to the OTHER provider if its primary fails (so the
# pipeline still completes when, e.g., the OpenAI key is over quota). The
# References / Bibliography section is skipped entirely — academic refs
# must NEVER be paraphrased or "humanised". Protected terms (drug names,
# p-values, citations, etc.) are surfaced as a hard constraint to every
# stage and verified post-hoc on the combined output.

# ---------------------------------------------------------------------------
# AI residual scoring
# ---------------------------------------------------------------------------
# After the 3-stage pipeline, we score the final text for residual AI
# patterns using two heuristics:
#   (1) Phrase density — how many of the known AI-filler phrases from
#       Stage B's blacklist still appear per 1000 words.
#   (2) Sentence-length uniformity — the coefficient of variation (CV) of
#       sentence word-counts. AI text has suspiciously uniform lengths
#       (CV ≈ 0.2–0.35); human academic prose is more erratic (CV > 0.45).
# Both sub-scores are combined into a 0–100 composite (0 = clean / human,
# 100 = AI-like) and bucketed into a labelled colour key.

_AI_RESIDUAL_PHRASES: tuple = (
    "moreover", "furthermore", "additionally", "in conclusion",
    "it is important to note", "it is worth noting", "notably",
    "it should be noted that", "it can be argued that",
    "generally speaking", "it has been shown that",
    "studies have demonstrated", "delve into",
    "in summary", "to summarize", "essentially",
    "nuanced", "multifaceted", "robust", "comprehensive",
    "tapestry", "underscore", "showcase", "leverage",
    "facilitate", "utilize", "overall",
)


def _score_ai_residual(text: str) -> Dict[str, Any]:
    """Heuristic AI-pattern residual score for the rewritten text.

    Returns dict:
      ``ai_risk_score``  int 0-100  (0 = very human-like, 100 = AI-like)
      ``ai_risk_label``  str        e.g. "Very Low", "Low", …
      ``ai_risk_key``    str        one of green / yellow / orange / red / gray
    """
    stripped = text.strip()
    if not stripped:
        return {"ai_risk_score": 0, "ai_risk_label": "N/A", "ai_risk_key": "gray"}

    lower = stripped.lower()
    word_count = max(1, len(re.findall(r"\b\w+\b", lower)))

    # ---- sub-score 1: phrase density (per 1000 words, capped at 100) ----
    phrase_hits = sum(lower.count(p) for p in _AI_RESIDUAL_PHRASES)
    phrase_score = min(100, round(phrase_hits / word_count * 1000))

    # ---- sub-score 2: sentence-length uniformity ----
    sentences = [s.strip() for s in re.split(r"[.!?]+", stripped)
                 if len(s.strip().split()) > 2]
    if len(sentences) >= 5:
        lengths = [len(s.split()) for s in sentences]
        mean_len = sum(lengths) / len(lengths)
        std_dev = (sum((l - mean_len) ** 2 for l in lengths) / len(lengths)) ** 0.5
        cv = std_dev / max(1.0, mean_len)
        # CV ≥ 0.55 → richly varied (0 risk); CV ≤ 0.20 → robot-flat (100 risk)
        uniformity_score = max(0, min(100, round((0.55 - cv) / 0.35 * 100)))
    else:
        uniformity_score = 20  # too few sentences to judge

    composite = round(0.55 * phrase_score + 0.45 * uniformity_score)

    if composite <= 12:
        label, key = "Very Low", "green"
    elif composite <= 30:
        label, key = "Low", "green"
    elif composite <= 55:
        label, key = "Moderate", "yellow"
    elif composite <= 75:
        label, key = "High", "orange"
    else:
        label, key = "Very High", "red"

    return {"ai_risk_score": composite, "ai_risk_label": label, "ai_risk_key": key}

