"""Plagiarism & AI-likelihood analysis service.

This module wraps two LLM providers — OpenAI and Google Gemini — behind a
small functional API used by the ``/api/plagiarism`` routes. The two
providers are interchangeable per call: callers pick "openai", "gemini",
or "auto" (try OpenAI first, fall back to Gemini).

Important honesty note
----------------------
Pure-LLM "plagiarism detection" cannot search the web or compare against
a copyrighted corpus. What this service actually does is:

  * Score how *AI-generated-sounding* the text reads (prompt patterns,
    boilerplate phrases, generic transitions).
  * Score how *unoriginal/templated* the prose looks against common
    academic clichés.
  * Flag specific passages and propose rewrites.

The combined "overall_score" is a heuristic the model returns. We surface
it transparently in the UI rather than pretending it's an authoritative
match against a database.
"""

